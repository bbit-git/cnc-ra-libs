void ADPCM_Init(ADPCMState* state)
{
    if (state) {
        state->index = 0;
        state->predicted = 0;
    }
}

int ADPCM_Decode(const uint8_t* src, int16_t* dst, int num_bytes,
                 ADPCMState* state)
{
    if (!src || !dst || !state || num_bytes <= 0) {
        return 0;
    }

    unsigned long fastindex = state->index;
    long sample = state->predicted;
    int samples_written = 0;

    for (int i = 0; i < num_bytes; i++) {
        uint8_t token = *src++;

        /* First nibble (lower 4 bits) */
        fastindex += (token & 0x0F);
        sample += DiffTable[fastindex];
        fastindex = IndexTable[fastindex];

        if (sample > 32767) sample = 32767;
        if (sample < -32768) sample = -32768;
        *dst++ = static_cast<int16_t>(sample);
        samples_written++;

        /* Second nibble (upper 4 bits) */
        fastindex += (token >> 4);
        sample += DiffTable[fastindex];
        fastindex = IndexTable[fastindex];

        if (sample > 32767) sample = 32767;
        if (sample < -32768) sample = -32768;
        *dst++ = static_cast<int16_t>(sample);
        samples_written++;
    }

    state->index = fastindex;
    state->predicted = sample;

    return samples_written;
}

