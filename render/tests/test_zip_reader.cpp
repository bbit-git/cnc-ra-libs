TEST(ZipReader_Lifecycle) {
    ZipReader reader;
    EXPECT_TRUE(reader.Open(test_zip, test_zip_len));
    reader.Close();
    
    // Check second Open after Close works correctly
    EXPECT_TRUE(reader.Open(test_zip, test_zip_len));
    EXPECT_EQ(reader.File_Count(), 1);
    
    // Check Get_Filename
    const char* f1 = reader.Get_Filename(0);
    EXPECT_NOT_NULL(f1);
    EXPECT_STR_EQ(f1, "test.txt");
    
    PASS();
}

TEST(ZipReader_AccessAndExtraction) {
    ZipReader reader;
    EXPECT_TRUE(reader.Open(test_zip, test_zip_len));
    
    EXPECT_EQ(reader.File_Count(), 1);
    EXPECT_EQ(reader.File_Size(0), 12);
    
    // Find missing
    EXPECT_EQ(reader.Find("missing.txt"), -1);
    
    // Case-insensitive find
    int idx = reader.Find("TEST.TXT");
    EXPECT_EQ(idx, 0);
    
    // Extract by name
    size_t size = 0;
    void* data = reader.Extract("TeSt.txT", &size);
    EXPECT_NOT_NULL(data);
    EXPECT_EQ(size, 12);
    EXPECT_MEM_EQ(data, "hello world\n", 12);
    free(data);
    
    PASS();
}

